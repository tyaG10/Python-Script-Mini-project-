def prRed(skk)->str: return ("\033[31m{}\033[00m" .format(skk)) 
def prGreen(skk)->str: return("\033[92m{}\033[00m" .format(skk)) 
def prYellow(skk)->str: return("\033[93m{}\033[00m" .format(skk)) 
def prLightPurple(skk)->str: return("\033[94m{}\033[00m" .format(skk)) 
def prPurple(skk)->str: return("\033[95m{}\033[00m" .format(skk)) 
def prCyan(skk)->str: return("\033[96m{}\033[00m" .format(skk)) 
def prLightGray(skk)->str: return("\033[97m{}\033[00m" .format(skk)) 
def prBlack(skk)->str: return("\033[98m{}\033[00m" .format(skk)) 

def prEnd()->str: return("\033[00m") 
